#include "../include/libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	char			*p;
	char			*str;

	if (!s || !f)
		return (NULL);
	str = (char *)malloc(sizeof(char) * ft_strlen(s) + 1);
	if (str == NULL)
		return (NULL);
	p = str;
	while (*s)
	{
		*p = f(p - str, *s);
		p++;
		s++;
	}
	*p = '\0';
	return (str);
}
