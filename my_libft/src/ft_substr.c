#include "../include/libft.h"

int	minimum(int a, int b)
{
	if (a < b)
		return (a);
	return (b);
}

char	*ft_substr(char const *s, unsigned int start, size_t len)
{
	char	*p;
	char	*str;
	size_t	slen;

	if (!s)
		return (NULL);
	slen = ft_strlen(s);
	if (start >= slen)
		return (ft_strdup(""));
	len = minimum(len, slen - start);
	str = (char *)malloc(sizeof(char) * len + 1);
	if (str == NULL)
		return (NULL);
	p = str;
	while (len > 0)
	{
		*p++ = s[start++];
		len--;
	}
	*p = '\0';
	return (str);
}
